package com.secondhand.vo;

public class Picture {
	
	private int id;
	private int good_id;
	private String pic_path;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGood_id() {
		return good_id;
	}
	public void setGood_id(int goodId) {
		good_id = goodId;
	}
	public String getPic_path() {
		return pic_path;
	}
	public void setPic_path(String picPath) {
		pic_path = picPath;
	}
	
}
